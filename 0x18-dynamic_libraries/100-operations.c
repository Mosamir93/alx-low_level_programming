#include <stdio.h>

/**
 * add - adds 2 ints
 * @a: first int
 * @b: second int
 * Return: sum of a and b
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts 2 ints
 * @a: first int
 * @b: second int
 * Return: subtraction of a and b
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - adds 2 ints
 * @a: first int
 * @b: second int
 * Return: multplication of a and b
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides 2 ints
 * @a: first int
 * @b: second int
 * Return: division of a and b
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mods 2 ints
 * @a: first int
 * @b: second int
 * Return: remainder of dividing a by b
 */

int mod(int a, int b)
{
	return (a % b);
}
